package basics;

public class Days {
	public static void main(String[] args) {
		
		String DayOfWeek = "Tuesday";
		
		switch (DayOfWeek) {
		case "Monday" : System.out.println("Second Day of week");
		break;
		case "Tuesday" : System.out.println("Third Day of week");
		break;
		case "Wednesday" : System.out.println("Fourth Day of week");
		break;
		
		}
			
		
	}

}
